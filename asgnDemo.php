<?php
if (!extension_loaded("curl")) {
  die("enable library curl first");
}

$year = "10";
$url = "http://127.0.0.1:8080/api/discountCalculator/$year";   

$curl = curl_init($url);
curl_setopt($curl, CURLOPT_RETURNTRANSFER, true);
$response = curl_exec($curl);   
curl_close($curl);

echo "Discount: $response";
?>